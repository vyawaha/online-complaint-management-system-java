package model;

/**
 * Defines the urgency level of complaints.
 *
 * CRITICAL:
 * Immediate attention required.
 *
 * HIGH:
 * Should be handled quickly.
 *
 * MEDIUM:
 * Normal priority issue.
 *
 * LOW:
 * Minor issue or suggestion.
 */
public enum ComplaintPriority {

    CRITICAL,

    HIGH,

    MEDIUM,

    LOW

}